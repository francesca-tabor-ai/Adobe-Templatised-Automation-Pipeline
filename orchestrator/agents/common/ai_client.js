// ---- AIClient class ----

/**
 * @param {string} providerName - 'google_gemini' | 'anthropic_claude' | 'openai'
 * @param {Object} [overrideConfig] - Override provider config fields
 */
function AIClient(providerName, overrideConfig) {
  var providersConfig = loadProviders();
  var provider = providersConfig.providers[providerName];
  if (!provider) throw new Error('Unknown AI provider: ' + providerName);

  this.providerName = providerName;
  this.config = Object.assign({}, provider, overrideConfig || {});
  this.apiKey = process.env[this.config.apiKeyEnvVar] || null;
  this.dryRun = !this.apiKey;
  this.rateLimiter = new RateLimiter(
    this.config.rateLimits ? this.config.rateLimits.requestsPerMinute : 60
  );
}

/**
 * Generate text completion.
 * @param {Object} options
 * @param {string} options.prompt
 * @param {string} [options.model]
 * @param {number} [options.temperature]
 * @param {number} [options.maxTokens]
 * @param {string} [options.responseFormat] - 'json' | 'text'
 * @param {number} [options.costEstimate]
 * @returns {Promise<{ text: string, usage: Object, cost: number }>}
 */
AIClient.prototype.generateText = function (options) {
  var self = this;
  if (self.dryRun) return mockGenerateText(options);

  var model = options.model || (self.config.models && self.config.models.text);
  var apiKey = self.apiKey;
  var baseUrl = self.config.baseUrl;
  var retryConfig = self.config.retryConfig;

  return self.rateLimiter.acquire().then(function () {
    var callFn;
    if (self.providerName === 'google_gemini') {
      callFn = function () { return callGoogleGeminiText(apiKey, baseUrl, model, options); };
    } else if (self.providerName === 'anthropic_claude') {
      callFn = function () { return callAnthropicClaude(apiKey, baseUrl, model, options); };
    } else if (self.providerName === 'openai') {
      callFn = function () { return callOpenAIText(apiKey, baseUrl, model, options); };
    } else {
      return Promise.reject(new Error('Unsupported provider for text: ' + self.providerName));
    }
    return withRetry(callFn, retryConfig);
  });
};

/**
 * Generate an image.
 * @param {Object} options
 * @param {string} options.prompt
 * @param {string} [options.model]
 * @param {number} [options.width]
 * @param {number} [options.height]
 * @param {string} [options.negativePrompt]
 * @param {string} [options.outputPath]
 * @param {string} [options.safetyFilterLevel]
 * @param {number} [options.costEstimate]
 * @returns {Promise<{ filePath: string, cost: number }>}
 */
AIClient.prototype.generateImage = function (options) {
  var self = this;
  if (self.dryRun) return mockGenerateImage(options);

  var model = options.model || (self.config.models && self.config.models.image);
  var apiKey = self.apiKey;
  var baseUrl = self.config.baseUrl;
  var retryConfig = self.config.retryConfig;

  return self.rateLimiter.acquire().then(function () {
    var callFn;
    if (self.providerName === 'google_gemini') {
      callFn = function () { return callGoogleImagen(apiKey, baseUrl, model, options); };
    } else if (self.providerName === 'openai') {
      callFn = function () { return callOpenAIImage(apiKey, baseUrl, model, options); };
    } else {
      return Promise.reject(new Error('Unsupported provider for images: ' + self.providerName));
    }
    return withRetry(callFn, retryConfig);
  });
};

/**
 * Generate text from a multimodal input (text + image).
 * Sends the image inline to the model for visual analysis.
 * @param {Object} options
 * @param {string} options.prompt - Text prompt
 * @param {string} options.imagePath - Path to the image file to analyze
 * @param {string} [options.model]
 * @param {number} [options.temperature]
 * @param {number} [options.maxTokens]
 * @param {string} [options.responseFormat] - 'json' | 'text'
 * @param {number} [options.costEstimate]
 * @returns {Promise<{ text: string, usage: Object, cost: number }>}
 */
AIClient.prototype.generateMultimodal = function (options) {
  var self = this;
  if (self.dryRun) return mockGenerateMultimodal(options);

  var model = options.model || (self.config.models && self.config.models.multimodal) || (self.config.models && self.config.models.text);
  var apiKey = self.apiKey;
  var baseUrl = self.config.baseUrl;
  var retryConfig = self.config.retryConfig;

  // Read image and convert to base64
  var imageBase64 = null;
  var imageMimeType = 'image/png';
  if (options.imagePath && fs.existsSync(options.imagePath)) {
    imageBase64 = fs.readFileSync(options.imagePath).toString('base64');
    var ext = path.extname(options.imagePath).toLowerCase();
    if (ext === '.jpg' || ext === '.jpeg') imageMimeType = 'image/jpeg';
    else if (ext === '.webp') imageMimeType = 'image/webp';
    else if (ext === '.gif') imageMimeType = 'image/gif';
  }

  return self.rateLimiter.acquire().then(function () {
    if (self.providerName === 'google_gemini') {
      var callFn = function () {
        return callGoogleGeminiMultimodal(apiKey, baseUrl, model, {
          prompt: options.prompt,
          imageBase64: imageBase64,
          imageMimeType: imageMimeType,
          temperature: options.temperature,
          maxTokens: options.maxTokens,
          responseFormat: options.responseFormat,
          costEstimate: options.costEstimate
        });
      };
      return withRetry(callFn, retryConfig);
    }
    // Fallback: text-only for providers without multimodal
    return self.generateText(options);
  });
};

/**
 * Check if client is in dry-run mode (no API key set).
 * @returns {boolean}
 */
AIClient.prototype.isDryRun = function () {
  return this.dryRun;
};

module.exports = { AIClient: AIClient };
